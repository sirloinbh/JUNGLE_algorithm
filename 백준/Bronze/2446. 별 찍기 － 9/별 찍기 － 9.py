# 입력: 첫째 줄에 N(1 ≤ N ≤ 100)이 주어진다.
n = int(input())

# 상단 패턴 출력
for i in range(n, 0, -1):
    # 공백 출력: n-i만큼
    print(' ' * (n-i), end='')
    # 별 출력: (2*i-1)만큼 (양 끝에 별을 포함하여 2배 - 1)
    print('*' * (2*i-1))

# 하단 패턴 출력 (첫 줄은 이미 출력했으므로 2부터 시작)
for i in range(2, n+1):
    # 공백 출력: n-i만큼
    print(' ' * (n-i), end='')
    # 별 출력: (2*i-1)만큼
    print('*' * (2*i-1))