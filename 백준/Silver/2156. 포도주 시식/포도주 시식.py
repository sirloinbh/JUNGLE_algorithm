n = int(input())  # 포도주 잔의 개수 입력
wine = [0] + [int(input()) for _ in range(n)]  # 포도주 양 정보 (인덱스를 1부터 사용하기 위해 앞에 0 추가)

# dp[i]: i번째 포도주까지 고려했을 때, 마실 수 있는 포도주의 최대 양
# 초기화: dp[0] = 0, dp[1] = 첫 번째 잔의 포도주 양
dp = [0] * (n+1)
dp[1] = wine[1]

# 포도주가 1잔 또는 2잔만 있을 경우를 고려
if n > 1:
    dp[2] = wine[1] + wine[2]

# 3번째 잔부터 시작하여 동적 프로그래밍 수행
for i in range(3, n+1):
    # 현재 잔을 선택하지 않는 경우
    # 현재 잔과 바로 이전 잔을 선택하고, 그 이전 잔은 선택하지 않는 경우
    # 현재 잔과 이전 잔을 선택하지 않고, 그 이전 잔만 선택하는 경우
    dp[i] = max(dp[i-1], dp[i-2] + wine[i], dp[i-3] + wine[i-1] + wine[i])

print(dp[n])  # 결과 출력